void	ft_swap(int	*a, int *b)
{
	int	temp;
	
	temp = *a;
	*a = *b;
	*b = temp;
}

#include <stdio.h>

int main()
{
	int s;
	int d;

	s = 3;
	d = 1;
	ft_swap(&s, &d);
	printf("%d", s);
}
